    public struct VehicleActions
    {
        private @PlayerInputAction m_Wrapper;
        public VehicleActions(@PlayerInputAction wrapper) { m_Wrapper = wrapper; }
        public InputAction @Drive => m_Wrapper.m_Vehicle_Drive;
        public InputAction @Reset => m_Wrapper.m_Vehicle_Reset;
        public InputActionMap Get() { return m_Wrapper.m_Vehicle; }
        public void Enable() { Get().Enable(); }
        public void Disable() { Get().Disable(); }
        public bool enabled => Get().enabled;
        public static implicit operator InputActionMap(VehicleActions set) { return set.Get(); }
        public void SetCallbacks(IVehicleActions instance)
        {
            if (m_Wrapper.m_VehicleActionsCallbackInterface != null)
            {
                @Drive.started -= m_Wrapper.m_VehicleActionsCallbackInterface.OnDrive;
                @Drive.performed -= m_Wrapper.m_VehicleActionsCallbackInterface.OnDrive;
                @Drive.canceled -= m_Wrapper.m_VehicleActionsCallbackInterface.OnDrive;
                @Reset.started -= m_Wrapper.m_VehicleActionsCallbackInterface.OnReset;
                @Reset.performed -= m_Wrapper.m_VehicleActionsCallbackInterface.OnReset;
                @Reset.canceled -= m_Wrapper.m_VehicleActionsCallbackInterface.OnReset;
            }
            m_Wrapper.m_VehicleActionsCallbackInterface = instance;
            if (instance != null)
            {
                @Drive.started += instance.OnDrive;
                @Drive.performed += instance.OnDrive;
                @Drive.canceled += instance.OnDrive;
                @Reset.started += instance.OnReset;
                @Reset.performed += instance.OnReset;
                @Reset.canceled += instance.OnReset;
